# 5. not
# 6. and
# 7. or

# Системы счисления (через встроенные функции)
num_b = bin(10)  # 2-чная система
print(num_b)
num_o = oct(10)  # 8-ричная система
print(num_o)
num_x = hex(10)  # 16-ричная система
print(num_x)
# Все функции возвращают строки

# Системы счисления (через форматируемые строки)
num_b_2 = f'{10:b}'
num_o_2 = f'{10:o}'
